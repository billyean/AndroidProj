package com.haibo.mobile.android.twitterredux.fragments;

import android.content.Context;
import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

/**
 * Created by Haibo(Tristan) Yan on 10/8/17.
 */

public class TweetsPageAdapter extends FragmentPagerAdapter {
    private static final String[] tabTitles = new String[] {
        "Home Timeline", "Mentions Timeline"
    };

    private Context context;

    public TweetsPageAdapter(FragmentManager fm, Context context) {
        super(fm);
        this.context = context;
    }

    @Override
    public Fragment getItem(int position) {
        switch (position) {
            case 0:
                return new HomeTimelineFragment();
            case 1:
                return new MentionsTimelineFragment();
        }
        return null;
    }

    @Override
    public int getCount() {
        return tabTitles.length;
    }

    @Override
    public CharSequence getPageTitle(int position) {
        return tabTitles[position];
    }
}
